dia_da_semana = int(input("Digite o número do dia da semana (1-7): "))

if dia_da_semana == 1:
    print("Segunda-feira")
elif dia_da_semana == 2:
    print("Terça-feira")
elif dia_da_semana == 3:
    print("Quarta-feira")
elif dia_da_semana == 4:
    print("Quinta-feira")
elif dia_da_semana == 5:
    print("Sexta-feira")
elif dia_da_semana == 6:
    print("Sábado")
elif dia_da_semana == 7:
    print("Domingo")
else:
    print("Dia inválido")